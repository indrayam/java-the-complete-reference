package me;

public class VarDemo {
    public static void main(String[] args) {
        var avg = 10.0;
        System.out.printf("Value of avg: %.2f%n", avg);

        int var = 1;
        System.out.printf("Value of var: %d%n", var);

        var k = -var;
        System.out.printf("Value of k: %d%n", k);
    }
}
